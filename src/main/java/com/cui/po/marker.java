package com.cui.po;

public class marker {
    public String x;
    public String y;
    public String describe;
    public marker(){
        super();
    }

    public String getDescribe() {
        return describe;
    }

    public void setDescribe(String describe) {
        this.describe = describe;
    }

    public String getY() {

        return y;
    }

    public void setY(String y) {
        this.y = y;
    }

    public String getX() {

        return x;
    }

    public void setX(String x) {
        this.x = x;
    }
}
